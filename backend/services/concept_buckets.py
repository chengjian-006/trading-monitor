"""炒作概念·大类归并 — 把同花顺细分涨停题材合并成少数几条主线大类。

情绪温度表(theme_heat)默认按"涨停题材首标签"细粒度统计(覆铜板/PCB/电子布各一列),
本模块提供"细题材 → 大类桶"的映射, 让前端可切到"合并大类"视角(一个桶吃下整条链),
未归入任何大类的小众题材统一进「其他」。

只读纯函数, 无副作用; 在 /theme-heat 接口读取时套用, 历史数据不动、随时可回退。
口径与盘点脚本 diag_zt_concept_heat 保持一致, 便于两表互相印证。
"""

OTHER = "其他"

# 大类桶: (桶名, 关键词列表)。自上而下首个命中即归入, 故越靠前优先级越高。
# 输入是单个细题材字符串(涨停原因首标签, 如 "高速覆铜板"/"玻璃基板"/"存储芯片涨价"),
# 用子串命中关键词归桶。members(关键词)同时回传前端做"自选概念命中"匹配。
THEME_BUCKETS: list[tuple[str, list[str]]] = [
    ("PCB·覆铜板", ["覆铜板", "PCB", "铜箔", "电子布", "PET铜箔", "HDI", "PI膜", "PI薄膜", "钻针", "电极箔"]),
    ("玻璃基板·面板", ["玻璃基板", "玻璃基封", "基板玻璃", "TGV", "UTG", "先进封装", "封装基板",
                       "FCBGA", "载板", "Micro-LED", "面板", "MED", "光伏玻璃", "浮法玻璃"]),
    ("半导体·存储", ["存储", "半导体", "HBM", "MLCC", "电子陶瓷", "晶圆", "SPM", "洁净室", "电子化学品", "芯片"]),
    ("AI算力·液冷", ["算力", "液冷", "数据中心", "光模块", "智算", "服务器", "IDC", "CPO", "光互连",
                     "AI应用", "AI智能体"]),
    ("机器人", ["机器人", "人形", "减速器", "丝杠"]),
    ("商业航天·军工", ["商业航天", "3D打印", "航天", "军工", "无人机", "航空装备", "低空经济", "VR/XR"]),
    ("核电·核聚变", ["可控核聚变", "核聚变", "核电", "中核"]),
    ("固态电池·锂电", ["固态电池", "锂电", "电解液", "隔膜", "芳纶", "能源金属", "碳酸锂"]),
    ("光纤·特高压", ["光纤", "光缆", "特高压"]),
    ("央企·重组", ["中字头", "央企", "国资", "国企", "资产重组", "资产置换", "控制权", "重整", "并购"]),
]

_BUCKET_KEYWORDS: dict[str, list[str]] = {name: kws for name, kws in THEME_BUCKETS}

# 子串误命中护栏: 含桶关键词却语义不属该桶的细分概念, 强制归「其他」不进桶。
# 例: "虚拟机器人"(同花顺等 AI 软件/智能投顾概念)含"机器人"子串, 但它是软件概念、
#     非硬件机器人板块, 不该被吸进机器人桶去蹭人形机器人板块的涨停热度(300033 同花顺曾误标)。
_MISCLASSIFY_GUARD: set[str] = {"虚拟机器人"}


def classify(theme: str) -> str:
    """细题材字符串 → 大类桶名; 无命中 → '其他'。"""
    t = (theme or "").strip()
    if not t:
        return OTHER
    if t in _MISCLASSIFY_GUARD:
        return OTHER
    for name, kws in THEME_BUCKETS:
        if any(k in t for k in kws):
            return name
    return OTHER


def bucket_keywords(name: str) -> list[str]:
    """桶的成员关键词(供前端「自选概念命中主线」匹配)。'其他' 返回空。"""
    return _BUCKET_KEYWORDS.get(name, [])
